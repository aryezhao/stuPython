"""
注意：
    三引号形式的字符串支持换行
"""

a = 'hello world'
print(a)
print(type(a))

b = "Tom"
print(type(b))

# 三引号
e = '''i am tom'''
print(type(e))

f = """I 
am Tom"""
print(type(f))
print(f)

c = "I'm Tom"
print(c)
print(type(c))

d = 'I\'m Tom'
print(d)
print(type(d))
